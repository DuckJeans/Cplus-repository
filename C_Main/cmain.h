struct Obj
{
	int x;
	int y;
	const char* shape[3];
};

struct Bullet
{
	bool act;
	int x;
	int y;
	const char* shape;
};


int main()
{
	Obj obj;
	obj.x = 10;
	obj.y = 10;
	obj.shape[0] = "   -----|-----";
	obj.shape[1] = "*>=====[_]L)";
	obj.shape[2] = "      -'-`-";

	Bullet bullets[Bullet_Count];
	for (int i = 0; i < Bullet_Count; i++)
	{
		//i == 0, 1, 2
		//bullets[0],bullets[1],bullets[2]
		bullets[i].act = false;
		bullets[i].x = i;
		bullets[i].y = 0;
		bullets[i].shape = "●";
	}


	while (true)
	{
		system("cls");

		for (int y = 0; y < 41; y++)
		{
			for (int x = 0; x < 41; x++)
			{
				switch (map[y][x])
				{
				case 1:
					SetPosition(x, y);
					printf("■");
					break;
				case 0:
					break;
				default:
					break;
				}
			}
		}

		if (GetAsyncKeyState(0x57))
		{
			obj.y--;
		}
		if (GetAsyncKeyState(0x41))
		{
			obj.x--;
		}

		if (GetAsyncKeyState(0x53))
		{
			obj.y++;
		}
		if (GetAsyncKeyState(0x44))
		{
			obj.x++;
		}

		if (GetAsyncKeyState(VK_SPACE))
		{
			for (int i = 0; i < Bullet_Count; i++)
			{
				if (bullets[i].act == false)
				{
					bullets[i].x = obj.x + 6;
					bullets[i].y = obj.y + 1;

					bullets[i].act = true;
					break; // for루프 빠져 나감
				}
			}
		}


		for (int i = 0; i < 3; i++)
		{
			SetPosition(obj.x, obj.y + i);
			printf(obj.shape[i]);
		}


		for (int i = 0; i < Bullet_Count; i++)
		{
			if (bullets[i].act == true)
			{
				bullets[i].x++;

				if (bullets[i].x >= 40)
				{
					bullets[i].act = false;
					bullets[i].y = 0;
					bullets[i].x = i;
				}
			}


			SetPosition(bullets[i].x, bullets[i].y);
			printf(bullets[i].shape);
		}



		Sleep(50);
	}

	return 0;
}

#pragma region WIN_API
void SetPosition(int x, int y)
{
	COORD pos;
	pos.X = x * 2;
	pos.Y = y;
	SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), pos);
}
#pragma endregion
